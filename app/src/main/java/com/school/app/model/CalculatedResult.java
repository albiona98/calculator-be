package com.school.app.model;

public class CalculatedResult {

  private Double netSalary;

  private Double grossSalary;

  private Double incomeTax;

  private Double employerSocialContribution;

  private Double employeeSocialContribution;

  private Double healthContribution;

  public Double getNetSalary() {
    return netSalary;
  }

  public void setNetSalary(Double netSalary) {
    this.netSalary = netSalary;
  }

  public Double getGrossSalary() {
    return grossSalary;
  }

  public void setGrossSalary(Double grossSalary) {
    this.grossSalary = grossSalary;
  }

  public Double getIncomeTax() {
    return incomeTax;
  }

  public void setIncomeTax(Double incomeTax) {
    this.incomeTax = incomeTax;
  }

  public Double getEmployerSocialContribution() {
    return employerSocialContribution;
  }

  public void setEmployerSocialContribution(Double employerSocialContribution) {
    this.employerSocialContribution = employerSocialContribution;
  }

  public Double getEmployeeSocialContribution() {
    return employeeSocialContribution;
  }

  public void setEmployeeSocialContribution(Double employeeSocialContribution) {
    this.employeeSocialContribution = employeeSocialContribution;
  }

  public Double getHealthContribution() {
    return healthContribution;
  }

  public void setHealthContribution(Double healthContribution) {
    this.healthContribution = healthContribution;
  }
}
